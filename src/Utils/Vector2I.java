package Utils;

/**Stores two integers
 * 
 * @author Barry
 *
 */
public class Vector2I {

	public int x;
	public int y;

	/**Stores two integer values
	 * 
	 */
	public Vector2I(int x, int y) {
		this.x = x;
		this.y = y;
	}
	
	/**
	 * Defaults to 0,0 values
	 */
	public Vector2I(){
		x=0;y=0;
	}
}
